// 21 种疼痛的快速索引（按区域）
export const PAIN_BY_REGION: Record<string, PainCondition[]> = PAIN_CONDITIONS.reduce((acc, c) => {
  if (!acc[c.region]) acc[c.region] = [];
  acc[c.region].push(c);
  return acc;
}, {} as Record<string, PainCondition[]>);

// 按名称搜索
export const searchPain = (keyword: string): PainCondition[] => {
  if (!keyword) return PAIN_CONDITIONS;
  const k = keyword.toLowerCase();
  return PAIN_CONDITIONS.filter(c =>
    c.name.toLowerCase().includes(k) ||
    c.region.toLowerCase().includes(k) ||
    c.category.toLowerCase().includes(k) ||
    c.symptoms?.some(s => s.toLowerCase().includes(k)) ||
    c.causes?.some(s => s.toLowerCase().includes(k)) ||
    c.treatment.some(s => s.toLowerCase().includes(k))
  );
};
